#define SCREEN_WIDTH 128 // OLED display width, in pixels
#define SCREEN_HEIGHT 64 // OLED display height, in pixels

TwoWire I2Cone = TwoWire(0);
TwoWire I2Ctwo = TwoWire(1);

Adafruit_SSD1306 displayUp(SCREEN_WIDTH, SCREEN_HEIGHT, &I2Cone, -1);
Adafruit_SSD1306 displayDown(SCREEN_WIDTH, SCREEN_HEIGHT, &I2Ctwo, -1);
// this is the default address of the display(0x78) on back of display
#define OLED1 0x3C //Up
// this is after switching over the jumper.
#define OLED2 0x3D //Down


void displaysSetup() {

  // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if (!displayUp.begin(SSD1306_SWITCHCAPVCC, OLED1)) {
    Serial.println(F("SSD1306 allocation failed"));
    for (;;); // Don't proceed, loop forever
  }
  if (!displayDown.begin(SSD1306_SWITCHCAPVCC, OLED2)) {
    Serial.println(F("SSD1306 allocation failed"));
    for (;;); // Don't proceed, loop forever
  }
  //
  //  delay(100);
  //  displayUp.begin(SSD1306_SWITCHCAPVCC, OLED1);
  //  delay(100);
  displayUp.setRotation(2);
  //displayUp.setTextSize(1);
  //displayUp.setFont(&FreeMono12pt7b);
  displayUp.setFont(&FreeMono9pt7b);
  displayUp.setTextColor(WHITE);
  displayUp.display();
  //delay(2000); // Pause for 2 seconds
  displayUp.clearDisplay();
  displayUp.display();

  //  delay(100);
  //  displayDown.begin(SSD1306_SWITCHCAPVCC, OLED2);
  //  delay(100);
  //displayDown.setTextSize(2);
  //displayDown.setFont(&FreeMono12pt7b);
  displayDown.setFont(&FreeMono9pt7b);
  displayDown.setTextColor(WHITE);
  displayDown.display();
  //delay(2000); // Pause for 2 seconds
  displayDown.clearDisplay();
  displayDown.display();
}

